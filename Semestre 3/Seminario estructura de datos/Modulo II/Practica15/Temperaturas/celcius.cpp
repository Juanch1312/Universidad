#include "Celcius.h"


/*********************************************************************
* Comment
*********************************************************************/
void Celcius::setValue(double temp)
{
  this->temperatura =temp;
}
/*********************************************************************
* Comment
*********************************************************************/
double Celcius::getKelvin()
{
  return (this->temperatura+273.15);
}
/*********************************************************************
* Comment
*********************************************************************/
double Celcius::getFahrenheit()
{
  return (this->temperatura*9/5)+32;
}
